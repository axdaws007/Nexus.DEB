using Nexus.DEB.Domain.Interfaces;

namespace Nexus.DEB.Api.GraphQL.Cis
{
    public class CisResolver
    {
        public async Task<string?> GetCisOwnedByNameAsync([Parent] IOwnedBy entity, CisNameDataLoader dataLoader, CancellationToken cancellationToken)
        {
            return await dataLoader.LoadAsync(entity.OwnedById, cancellationToken);
        }
    }
}
